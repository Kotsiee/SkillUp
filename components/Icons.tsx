import { animate } from 'https://esm.sh/motion@11.15.0';
import { Component, createRef, JSX } from 'preact';

interface AIconProps {
  size?: number;
  colour?: string;
  startPaths: string[];
  endPaths?: string[];
  onClick?: (state: boolean) => void;
  onHover?: () => void;
  offHover?: () => void;
  initalState?: boolean;
  className?: string;
  style?: string | JSX.CSSProperties | JSX.SignalLike<string | JSX.CSSProperties | undefined>;
}

interface AIconState extends AIconProps {}

export default class AIcon extends Component<AIconProps, AIconState> {
  pRefs: SVGPathElement[] = [];
  svg = createRef<SVGSVGElement>();
  started: boolean = false;
  initPaths: string[] = [];

  constructor(props: any) {
    super(props);

    this.state = {
      size: props.size || 24,
      colour: props.colour || 'white',
      startPaths: props.startPaths,
      endPaths: props.endPaths,
      ...props,
    };
  }

  startClick() {
    if (this.pRefs) {
      this.pRefs.forEach((path, index) => {
        animate(
          path,
          { d: this.started ? this.state.startPaths[index] : this.state.endPaths![index] },
          { duration: 0.2 }
        );
      });
    }

    if (!this.started) this.svg?.current?.classList.add('active');
    else this.svg?.current?.classList.remove('active');
  }

  click() {
    if (this.state.endPaths) this.startClick();
    if (this.state.onClick) this.state.onClick(this.started);

    this.started = !this.started;
  }

  override componentDidMount(): void {
    this.started = this.state.initalState ? this.state.initalState : false;

    if (this.started) {
      this.svg?.current?.classList.add('active');
      this.pRefs.forEach((path, index) => {
        path.setAttribute(
          'd',
          `${
            this.state.endPaths?.[index] ? this.state.endPaths[index] : this.state.startPaths[index]
          }`
        );
      });
    } else {
      this.svg?.current?.classList.remove('active');
    }
  }

  override componentDidUpdate(prevProps: AIconProps) {
    // Compare the old style with the new style
    if (prevProps.style !== this.props.style) {
      this.props.style = prevProps.style;
    }
  }

  onMouseEnter = () => {
    if (this.state.onHover) this.state.onHover();
  };
  onMouseLeave = () => {
    if (this.state.offHover) this.state.offHover();
  };

  render() {
    const { size, colour, startPaths } = this.state;

    return (
      <svg
        ref={this.svg}
        width={size}
        height={size}
        viewBox="0 0 64 64"
        fill-rule="evenodd"
        xmlns="http://www.w3.org/2000/svg"
        stroke={colour}
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
        class={`${this.state.className} icon animatedIcon`}
        onClick={() => this.click()}
        onMouseEnter={this.onMouseEnter}
        onMouseLeave={this.onMouseLeave}
        style={this.props.style}
      >
        {startPaths!.map((d, index) => {
          return (
            <path
              key={index}
              ref={el => el && (this.pRefs[index] = el)}
              strokeLinecap="round"
              strokeLinejoin="round"
              d={d}
            />
          );
        })}
      </svg>
    );
  }
}

